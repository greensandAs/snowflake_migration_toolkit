            COUNT(IFF(f.RULE_CONFIG_ID = 3452, 1, NULL)) = 0 AND MAX(IFF(rr.RULE_CONFIG_ID = 3452, rr.IS_SUCCESS, NULL)) IS NULL
            THEN 'NOT CONFIGURED'
        WHEN COUNT(IFF(f.RULE_CONFIG_ID = 3452, 1, NULL)) = 0 AND MAX(IFF(rr.RULE_CONFIG_ID = 3452, rr.IS_SUCCESS, NULL)) IS NOT NULL THEN 'PASS' WHEN
            MAX(IFF(f.RULE_CONFIG_ID = 3452, 1, 0)) = 1
