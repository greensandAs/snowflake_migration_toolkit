        WHEN COUNT(IFF(f.RULE_CONFIG_ID = 3453, 1, NULL)) = 0 AND MAX(IFF(rr.RULE_CONFIG_ID = 3453, rr.IS_SUCCESS, NULL)) IS NOT NULL THEN 'PASS' WHEN
