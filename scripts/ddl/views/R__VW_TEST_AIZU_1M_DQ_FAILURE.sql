SELECT
    f.DATASET_RUN_ID,
    344 AS DATASET_ID,
    'TEST_AIZU_1M' AS DATASET_NAME,
    CASE
        WHEN
            COUNT(IFF(f.RULE_CONFIG_ID = 3453, 1, NULL)) = 0 AND MAX(IFF(rr.RULE_CONFIG_ID = 3453, rr.IS_SUCCESS, NULL)) IS NULL
