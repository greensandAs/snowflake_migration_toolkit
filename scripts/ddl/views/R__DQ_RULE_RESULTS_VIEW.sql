USE DATABASE {{ snowflake_database }};

USE SCHEMA {{ snowflake_schema }};

CREATE OR REPLACE VIEW DQ_RULE_RESULTS_VIEW (
    BATCH_ID,
    DATASET_RUN_ID,
    DATASET_ID,
    RULE_CONFIG_ID,
    EXPECTATION_ID,
    RUN_NAME,
    RUN_TIMESTAMP,
    DATASET_NAME,
    EXPECATION_CONFIG,
    IS_SUCCESS,
    RESULTS,
    EXPECTATION_NAME,
    DETAILS,
    ELEMENT_COUNT,
    MISSING_COUNT,
    MISSING_PERCENT,
    OBSERVED_VALUE,
    PARTIAL_UNEXPECTED_COUNTS,
    PARTIAL_UNEXPECTED_INDEX_LIST,
    PARTIAL_UNEXPECTED_LIST,
    UNEXPECTED_COUNT,
    UNEXPECTED_PERCENT,
    UNEXPECTED_PERCENT_NONMISSING,
    UNEXPECTED_PERCENT_TOTAL,
    UNEXPECTED_ROWS,
    DATA_ROWS,
    USER,
    DIMENSION,
    FAILED_ROWS,
    SESSION_ID,
    RUN_DATE
) AS
SELECT
    *,
    TO_DATE(RUN_TIMESTAMP) AS RUN_DATE
FROM DQ_RULE_RESULTS;
